public class OneTwoALot {
    public static void main(String[] args) {
        int a = 6;
        if (a <= 0) {
            System.out.println("The number is Not enough");

        } else if (a == 1) {
            System.out.println("One");

        } else if (a == 2) {
            System.out.println("Two");

        } else if (a > 2) {
            System.out.println("A lot");
        }
    }

}
